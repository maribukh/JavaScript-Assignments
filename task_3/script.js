
// for ციკლის გამოყენება
const number = +prompt("Enter Number:");
console.log("from 1 " + number + "-to number ^:");
for (let i = 1; i <= number; i++) {
    console.log(i * i); // კვადრატის გამოთვლა და გამოყვანა
}

// while ციკლის გამოყენება
let i = 1; 
console.log("from 1 " + number + "-to number ^:");
while (i <= number) {
    console.log(i * i); 
    i++; 
}
